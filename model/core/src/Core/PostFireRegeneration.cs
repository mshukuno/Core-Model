namespace Landis.Core
{
    /// <summary>
    ///    Forms of cohort reproduction that can occur after fires.
    /// </summary>
    public enum PostFireRegeneration
    {
        /// <summary>
        /// No forms of reproduction occur after fires.
        /// </summary>
        None = 0,

        /// <summary>
        /// Resprouting can occur after fires.
        /// </summary>
        Resprout,

        /// <summary>
        /// Serotiny can occur after fires.
        /// </summary>
        Serotiny
    }
}
